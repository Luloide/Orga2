#include <stdio.h>

int main(){

    printf("caso i++\n");
    int i = 0;
    printf("durante la incrementacion me devuelve: %i\n",i++);
    printf("despues de incrementarlo el valor de i es: %i\n",i);

    printf("caso ++i\n");
    i = 0;
    printf("durante la incrementacion me devuelve: %i\n",++i);
    printf("despues de incrementarlo el valor de i es: %i\n",i);


    return 0;
}
